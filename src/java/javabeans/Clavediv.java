import java.math.BigDecimal;

public class Clavediv
{
    private Integer id_clave_div;
    private String clave_div;
    private String descripcion;
    private BigDecimal importe;
    
    public Clavediv(){ super(); }
    //constructor que permite crear un objeto
    //Mensaje a partir de los datos del mismo
    public Clavediv(String clave_div, String descripcion,BigDecimal importe,Integer id_clave_div)
    {
        this.clave_div=clave_div;
        this.descripcion=descripcion;
        this.id_clave_div=id_clave_div;
        this.importe=importe;
        
    }

    public String getClave_div() {
        return clave_div;
    }

    public void setClave_div(String clave_div) {
        this.clave_div = clave_div;
    }

    public String getDescripcion() {
        return descripcion;
    }

    public void setDescripcion(String descripcion) {
        this.descripcion = descripcion;
    }
    
    public Integer getId_clave_div() {
        return id_clave_div;
    }

    public void setId_clave_div(Integer id_clave_div) {
        this.id_clave_div = id_clave_div;
    }
    
    public BigDecimal getimporte() {
        return importe;
    }

    public void setimporte(BigDecimal importe) {
        this.importe = importe;
    }

}
